#include "libft.h"

void	ft_print_tab(char **tab)
{
	int		i;

	i = 0;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		ft_putstr(" ");
		i++;
	}
	ft_putchar('\n');
}
